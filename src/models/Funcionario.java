package models;

/**
 *
 * @author ecles
 */
public class Funcionario implements IModelo {
    
    private int id;
    private String email;
    private String name;
    private String cpf;

    public Funcionario() {
    }

    public Funcionario(int id, String email, String name, String cpf) {
        this.id = id;
        this.email = email;
        this.name = name;
        this.cpf = cpf;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getCpf() {
        return cpf;
    }

    public void setCpf(String cpf) {
        this.cpf = cpf;
    }
    
    
}
